/*
Operadores de Comparação
> maior
>= maior que ou igual a
< menor que
<= menor que ou igual a
== igualdade (valor)************* não recomendavel
=== igualdade estrita (valor e tipo)
!= diferente (valor)************* não recomendavel
!== diferente estrito (valor e tipo)
*/
console.log(10 > 5); //10 é maior do que 5?
console.log(10>=5); //10 é maior ou igual a 5?
console.log(10 < 5); //10 é menor do 5?
console.log(10 <= 5); //10 é menor ou igual a 5?
console.log(10-5 == '5'); // 10-5 é igual a 5?
console.log(10-5 === '5');
console.log(10 != '10');
console.log(10 !== '5');